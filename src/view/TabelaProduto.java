    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tbProduto.getSelectedRow();

        if (linhaSelecionada < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro");
        } else {
            int i = linhaSelecionada;
            Produto pro = produto.get(i);

            if (pro.nome.equals("Nome")) {
                JOptionPane.showMessageDialog(rootPane, "Não é possivel Alterar Produto");
                return;
            }

            FormularioProduto dialogo = new FormularioProduto(null, true, produto.get(i));

            dialogo.setVisible(true);
            if (dialogo.pro != null) {

                //pessoas.set(i, dialogo.p);
                prDao = new ProdutoDao();
                prDao.alterar(dialogo.pro);

                carregarTabelaProduto();
            }
        }

    }//GEN-LAST:event_btnAlterarActionPerformed

