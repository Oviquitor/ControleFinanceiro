    private void carregarTabelaProduto() {
        String condicao = " where 1=1 ";
        prDao = new ProdutoDao();

        String nome = tfNome.getText();
        if (!"".equals(nome)) {
            condicao += " and nome like '%" + nome + "%' ";
        }

        produto = prDao.listar(condicao);

        String[] colunas = new String[]{
            "nome", "codigoBarras", "unidade", "preco"
        };
        this.tbProduto.setModel(new DefaultTableModel(colunas, 0) {
            Class[] types = new Class[]{String.class, String.class, String.class, String.class};

            boolean[] canEdit = new boolean[]{
                false, false, false, false
            };

            @Override
            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        DefaultTableModel dtm = (DefaultTableModel) tbProduto.getModel();

        int linha = this.tbProduto.getRowCount();
        for (int c = 0; c < linha; c++) {
            dtm.removeRow(0);
        }
        for (Produto pro : produto) {
            Object[] dado = new Object[4];
            dado[0] = pro.nome;
            dado[1] = pro.codigoBarras;
            dado[2] = pro.unidade;
            dado[3] = pro.preco;

            dtm.addRow(dado);
        }

    }
