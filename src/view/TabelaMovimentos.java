    private void carregarTabelaMovimentos() {
        String condicao = " where 1=1 "
                + " and m.id_usuario = " + this.usuario.id
                + " and m.tipo = '" + this.tipo + "'"
                + " and m.data between '" + Util.dataFormatoSql(dfDataIni.getText()) + "'"
                + " and "
                + "'" + Util.dataFormatoSql(dfDataFin.getText()) + "'"
                + " order by data ";
        mDao = new MovimentosDao();

//        String nome = tfNome.getText();
//        if (!"".equals(nome)) {
//            condicao += " and nome like '%" + nome + "%' ";
//        }

        movimentos = mDao.listar(condicao);

        String[] colunas = new String[]{
            "Data", "Pessoa", "Obs", "Valor"
        };
        this.tbMovimentos.setModel(new DefaultTableModel(colunas, 0) {
            Class[] types = new Class[]{String.class, String.class, String.class, String.class};
            boolean[] canEdit = new boolean[]{
                false, false, false, false
            };

            @Override
            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        DefaultTableModel dtm = (DefaultTableModel) tbMovimentos.getModel();

        int linha = this.tbMovimentos.getRowCount();
        for (int c = 0; c < linha; c++) {
            dtm.removeRow(0);
        }

        Double total = 0.0;
        for (Movimentos m : movimentos) {
            total += m.valor;
            Object[] dado = new Object[4];
            dado[0] = m.data;
            dado[1] = m.nome_pessoa;
            dado[2] = m.obs;
            dado[3] = m.valorFormatado();

            dtm.addRow(dado);
        }
        vfTotal.setDoubleValue(total);

        tbMovimentos.getColumnModel().getColumn(0).setPreferredWidth(51);
        tbMovimentos.getColumnModel().getColumn(1).setPreferredWidth(172);
        tbMovimentos.getColumnModel().getColumn(2).setPreferredWidth(417);
        tbMovimentos.getColumnModel().getColumn(3).setPreferredWidth(62);

        tbMovimentos.getColumnModel().getColumn(3).setCellRenderer(Util.alinhaDireita());

    }
