    private void carregarTabelaPessoa() {
        String condicao = " where 1=1 ";
        pDao = new PessoaDao();

        String nome = tfNome.getText();
        if (!"".equals(nome)) {
            condicao += " and nome like '%" + nome + "%' ";
        }

        pessoas = pDao.listar(condicao);

        String[] colunas = new String[]{
            "Nome", "Tipo"
        };
        this.tbPessoa.setModel(new DefaultTableModel(colunas, 0) {
            Class[] types = new Class[]{String.class, String.class};

            boolean[] canEdit = new boolean[]{
                false, false
            };

            @Override
            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        DefaultTableModel dtm = (DefaultTableModel) tbPessoa.getModel();

        int linha = this.tbPessoa.getRowCount();
        for (int c = 0; c < linha; c++) {
            dtm.removeRow(0);
        }
        for (Pessoa p : pessoas) {
            Object[] dado = new Object[2];
            dado[0] = p.nome;
            dado[1] = p.tipo;

            dtm.addRow(dado);
        }

    }
