    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tbPessoa.getSelectedRow();

        if (linhaSelecionada < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro");
        } else {
            int i = linhaSelecionada;
            Pessoa p = pessoas.get(i);

            if (p.tipo.equals("Usuario")) {
                JOptionPane.showMessageDialog(rootPane, "Não é possivel Alterar usuario");
                return;
            }

            FormularioPessoa dialogo = new FormularioPessoa(null, true, pessoas.get(i));

            dialogo.setVisible(true);
            if (dialogo.p != null) {

                //pessoas.set(i, dialogo.p);
                pDao = new PessoaDao();
                pDao.alterar(dialogo.p);

                carregarTabelaPessoa();
            }
        }

    }//GEN-LAST:event_btnAlterarActionPerformed

